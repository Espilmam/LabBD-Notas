package modelo;

public class Aluno {
	
	private int codigo;
	private String nome;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getTipo() {
		return nome;
	}
	public void setTipo(String nome) {
		this.nome = nome;
	}
}
